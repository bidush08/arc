// --- ANIMATION ENGINE ---
const canvas = document.getElementById('starCanvas');
const ctx = canvas.getContext('2d');
let width, height, centerX, centerY, stars = [];
const numStars = 300, starSpeed = 2, fov = 300;

function initStars() {
    width = window.innerWidth; height = window.innerHeight;
    centerX = width / 2; centerY = height / 2;
    canvas.width = width; canvas.height = height;
    stars = [];
    for(let i=0; i<numStars; i++) stars.push({ x: (Math.random()-0.5)*width*1.5, y: (Math.random()-0.5)*height*1.5, z: Math.random()*width });
}

function animateWarp() {
    ctx.clearRect(0, 0, width, height);
    stars.forEach(star => {
        star.z -= starSpeed;
        if (star.z <= 0) { star.x = (Math.random()-0.5)*width*1.5; star.y = (Math.random()-0.5)*height*1.5; star.z = width; }
        const scale = fov / star.z;
        const sx = star.x * scale + centerX;
        const sy = star.y * scale + centerY;
        const size = (1 - star.z / width) * 3;
        const opacity = (1 - star.z / width);
        if (sx>0 && sx<width && sy>0 && sy<height) {
            ctx.fillStyle = `rgba(255, 200, 50, ${opacity})`;
            ctx.beginPath(); ctx.arc(sx, sy, size, 0, Math.PI*2); ctx.fill();
        }
    });
    const scroll = window.scrollY;
    const time = performance.now() * 0.005; 
    const r1 = time + (scroll * 0.15);  
    const r2 = -time + (scroll * -0.1); 
    const r3 = time + (scroll * 0.05);  
    document.getElementById('ring1').style.transform = `rotate(${r1}deg)`;
    document.getElementById('ring2').style.transform = `rotate(${r2}deg)`;
    document.getElementById('ring3').style.transform = `rotate(${r3}deg)`;
    requestAnimationFrame(animateWarp);
}
initStars(); animateWarp(); window.addEventListener('resize', initStars);

// --- CURSOR LOGIC ---
const cursorDot = document.getElementById('cursorDot');
const cursorOutline = document.getElementById('cursorOutline');
let mouseX = 0, mouseY = 0;
let outlineX = 0, outlineY = 0;

window.addEventListener('mousemove', (e) => {
    mouseX = e.clientX; mouseY = e.clientY;
    cursorDot.style.left = mouseX + 'px'; cursorDot.style.top = mouseY + 'px';
});

function animateCursor() {
    let distX = mouseX - outlineX; let distY = mouseY - outlineY;
    outlineX += distX * 0.25; 
    outlineY += distY * 0.25;
    cursorOutline.style.left = outlineX + 'px'; cursorOutline.style.top = outlineY + 'px';
    requestAnimationFrame(animateCursor);
}
animateCursor();

